function afficher_le_catalogue(){
  table= document.getElementById('catalogue_dynamique');
  for(element of liste_de_piece){
    var nouvelleLigne = table.insertRow(-1);
    nouvelleLigne.id=element.designation+' '+element.reference;
    //image
    var nouvelleCellule = nouvelleLigne.insertCell(-1);
    var img = document.createElement('img');
    img.className='Icones'
    img.src = element.image_path;
    nouvelleCellule.appendChild(img);
    //designation
    var nouvelleCellule = nouvelleLigne.insertCell(-1);
    var nouveauTexte = document.createTextNode(element.designation)
    nouvelleCellule.appendChild(nouveauTexte);
    //reference
    var nouvelleCellule = nouvelleLigne.insertCell(-1);
    var nouveauTexte = document.createTextNode(element.reference)
    nouvelleCellule.appendChild(nouveauTexte);
    //marque
    var nouvelleCellule = nouvelleLigne.insertCell(-1);
    var nouveauTexte = document.createTextNode(element.marque)
    nouvelleCellule.appendChild(nouveauTexte);
    //prix
    var nouvelleCellule = nouvelleLigne.insertCell(-1);
    var nouveauTexte = document.createTextNode(element.prix +' DH')
    nouvelleCellule.appendChild(nouveauTexte);



  }
}

function myFunction() {
// Declare variables
  var input, filter, table, tr, td, i, txtValue;
  input = document.getElementById("myInput");
  filter = input.value.toUpperCase();
  table = document.getElementById("catalogue_dynamique");
  tr = table.getElementsByTagName("tr");

// Loop through all table rows, and hide those who don't match the search query
  for (i = 0; i < tr.length; i++) {
    td = tr[i].getElementsByTagName("td")[1];
    if (td) {
      txtValue = tr[i].id;
      if (txtValue.toUpperCase().indexOf(filter) > -1) {
      tr[i].style.display = "";
      }
      else {
      tr[i].style.display = "none";
      }
    }
  }
}


